# Large Numbers

=begin
Question
Using the ruby documentation, determine how you can write large numbers in a way that makes them easier to read.

Solution
The documentation recommends using underscore characters to increase readability
  > for example, to replace commas:
      75930372
      75_930_372
These are stripped/ignored when the code is interpreted    

* note - supplied hint was to check literals documentation. this is @ 
https://docs.ruby-lang.org/en/2.7.0/syntax/literals_rdoc.html

=end